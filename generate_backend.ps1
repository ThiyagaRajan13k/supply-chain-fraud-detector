import com.finguard.entity.Role;
import com.finguard.entity.User;
import com.finguard.repository.UserRepository;
import com.finguard.security.JwtTokenProvider;
import jakarta.validation.Valid;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.http.ResponseEntity;
import org.springframework.security.authentication.AuthenticationManager;
import org.springframework.security.authentication.UsernamePasswordAuthenticationToken;
import org.springframework.security.core.Authentication;
import org.springframework.security.core.context.SecurityContextHolder;
import org.springframework.security.crypto.password.PasswordEncoder;
import org.springframework.web.bind.annotation.*;

@RestController
@RequestMapping("/api/auth")
public class AuthController {

    @Autowired
    private AuthenticationManager authenticationManager;

    @Autowired
    private UserRepository userRepository;

    @Autowired
    private PasswordEncoder passwordEncoder;

    @Autowired
    private JwtTokenProvider tokenProvider;

    @PostMapping("/login")
    public ResponseEntity<?> authenticateUser(@Valid @RequestBody AuthRequest loginRequest) {

        Authentication authentication = authenticationManager.authenticate(
                new UsernamePasswordAuthenticationToken(
                        loginRequest.getUsername(),
                        loginRequest.getPassword()
                )
        );

        SecurityContextHolder.getContext().setAuthentication(authentication);

        String jwt = tokenProvider.generateToken(authentication);
        
        User user = userRepository.findByUsername(loginRequest.getUsername()).orElseThrow();
        
        return ResponseEntity.ok(new AuthResponse(jwt, user.getUsername(), user.getRole().name(), user.getFullName()));
    }

    // Temporary init endpoint for setup purposes
    @PostMapping("/init")
    public ResponseEntity<?> initUsers() {
        if(userRepository.count() > 0) {
            return ResponseEntity.badRequest().body(new ApiResponse(false, "Users already initialized"));
        }

        createUser("admin", "admin123", "System Administrator", "admin@finguard.com", Role.ROLE_ADMIN);
        createUser("supplier", "supplier123", "Global Supplies Inc", "supplier@finguard.com", Role.ROLE_SUPPLIER);
        createUser("analyst", "analyst123", "Fraud Analyst 1", "analyst@finguard.com", Role.ROLE_FRAUD_ANALYST);
        createUser("bank", "bank123", "Officer BankCorp", "bank@finguard.com", Role.ROLE_BANK_OFFICER);

        return ResponseEntity.ok(new ApiResponse(true, "Users initialized successfully"));
    }
    
    private void createUser(String username, String password, String fullName, String email, Role role) {
        User user = new User();
        user.setUsername(username);
        user.setPassword(passwordEncoder.encode(password));
        user.setFullName(fullName);
        user.setEmail(email);
        user.setRole(role);
        userRepository.save(user);
    }
}
